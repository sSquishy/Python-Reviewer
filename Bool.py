# Comparison operators example
a = 10
b = 5

# Greater than (>)
print("Comparison (a > b):", a > b, "Result:", a, "is greater than", b)

# Less than (<)
print("Comparison (a < b):", a < b, "Result:", a, "is less than", b)

# Equal to (==)
print("Comparison (a == b):", a == b, "Result:", a, "is equal to", b)

# Not equal to (!=)
print("Comparison (a != b):", a != b, "Result:", a, "is not equal to", b)

# Greater than or equal to (>=)
print("Comparison (a >= b):", a >= b, "Result:", a, "is greater than or equal to", b)

# Less than or equal to (<=)
print("Comparison (a <= b):", a <= b, "Result:", a, "is less than or equal to", b)
