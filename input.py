# String input
string_input = input("Enter a string: ")
print("String input:", string_input, "Type:", type(string_input))

# Character input
char_input = input("Enter a character: ")
print("Character input:", char_input, "Type:", type(char_input))

# Integer input
int_input = int(input("Enter an integer: "))
print("Integer input:", int_input, "Type:", type(int_input))

# Float input
float_input = float(input("Enter a float: "))
print("Float input:", float_input, "Type:", type(float_input))

# Tuple input
tuple_input = tuple(input("Enter three elements for a tuple: "))
print("Tuple input:", tuple_input, "Type:", type(tuple_input))

# List input
list_input = list(input("Enter three elements for a list: "))
print("List input:", list_input, "Type:", type(list_input))

# Complex input
complex_input = input("Enter a complex number (in the form of a+bj): ")
print("Complex input:", complex_input, "Type:", type(complex_input))
